# MAGIC #### Project Goal and Deliverable

# COMMAND ----------

# MAGIC %md 
# MAGIC
# MAGIC - `Project Goal` 
# MAGIC   - is the outcome of a project / a roadmap
# MAGIC   - The goal should be well defined and clear & understood by all team member and stakeholder
# MAGIC - `Deliverable`
# MAGIC   - is a product / service created for customer / sponser / stakeholders
# MAGIC   - is a part of achiveing project goal
# MAGIC - By accessing stakeholder power grid, you will know how often to communicate with them
# MAGIC - Using [RAIC](https://www.teamgantt.com/blog/raci-chart-definition-tips-and-example#:~:text=A%20RACI%20chart%E2%80%94also%20known,Consulted) chart can help you understand and assign each team member's role and responsbility
# MAGIC - `SMART Goal`
# MAGIC   - Specific
# MAGIC     - What do I want to accomplish
# MAGIC     - why is this a goal
# MAGIC     - who is involved
# MAGIC     - where should the goal be delivered
# MAGIC   - Measurable
# MAGIC     - How much
# MAGIC     - How many
# MAGIC     - How would I know when it is accomplished
# MAGIC   - Achievable
# MAGIC   - Relevant
# MAGIC     - How can it be accomplished? helps you find out if a goal is attainable
# MAGIC   - Time-Bound
# MAGIC - `OKR`
# MAGIC   - Objective (What needs to be achived & best outcome) and Key Result (measurable outcomes define what has to be met)
# MAGIC   - It is a combination between goal and action items for each goal
# MAGIC   - It is a simplified SMART goal
# MAGIC   - **OKR should be revisited and monitored during project life cycle**
# MAGIC   - **For each key result, we should set a metric to measure it & we should assign each KR to a person; 60% to 70% achievement is a sweet spot of project achivement**
# MAGIC   - [Google OKR Playbooks](https://www.whatmatters.com/resources/google-okr-playbook)
# MAGIC   - [Smart Goals vs OKR](https://www.smartsheet.com/content/okr-vs-smart-goals)

