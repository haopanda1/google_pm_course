# MAGIC ### Project Methodologies

# COMMAND ----------

# MAGIC %md 
# MAGIC
# MAGIC #### Linear Project Method - Waterfall
# MAGIC - `Meaning`: the second task of the project can start when the first one is completed
# MAGIC - *it is suitable for project like building a house*
# MAGIC - The end result of project is predefined and it cannot change
# MAGIC - Waterfall approach has risk management tools to help dealing with project scope change
# MAGIC
# MAGIC #### Iterative Project Method - Agile
# MAGIC - `Meaning`: Tasks can happen at the same time while other tasks has been working on
# MAGIC - This method is flexible and end result can always change
# MAGIC - The agile approach starts in 90s when demand for fast delivery is asked
# MAGIC - Stakeholders have an idea what they want but don't have a solid picture yet
# MAGIC
# MAGIC #### Lean Six Sigma Method
# MAGIC - **This approach most used when you need identify the issue and find ways for improvement**
# MAGIC - `Lead Approach`
# MAGIC   - it is used to reduce cost / waste, limit resource, and streamline processes
# MAGIC   - Recognize eight types of waste (defects, non-utilized talent, excess processing, overproduction, inventory, transportation, wait)
# MAGIC   - It uses `5S` to address those wastes: 
# MAGIC     - Sort: Remove all items not needed for current production operations and leave only the bare essentials. 
# MAGIC     - Set in order: Arrange needed items so that they are easy to use. Label items so that anyone can find them or put them away. 
# MAGIC     - Shine: Keep everything in the correct place. Clean your workspace every day.
# MAGIC     - Standardize: Perform the process in the same way every time.
# MAGIC     - Sustain: Make a habit of maintaining correct procedures and instill this discipline in your team.
# MAGIC - `Six Sigma Approach`
# MAGIC   - it is used to ensure quality process is followed everytimes
# MAGIC   - Identify measurable KPI such as time, cost, and time spent and remove those don't match `six sigma` standard
# MAGIC     - Always focus on the customer.
# MAGIC     - Identify and understand how the work gets done. Understand how work really happens.
# MAGIC     - Make your processes flow smoothly.
# MAGIC     - Reduce waste and concentrate on value.
# MAGIC     - Stop defects by removing variation.
# MAGIC     - Involve and collaborate with your team.
# MAGIC     - Approach improvement activity in a systematic way.
# MAGIC - Lean Six Sigma Stages 
# MAGIC   - Define: Talk with stakeholders and find out issues
# MAGIC   - Measure: Collect data
# MAGIC   - Analyze: Analyze data and identify gaps and inefficiency in current approach
# MAGIC   - Improve: Present findings and get ready for improvement
# MAGIC   - Control: Learn from work you did and put new process and documentation in place
# MAGIC
# MAGIC #### Project Management Methodologies and When to Choose Which 
# MAGIC - [Link](https://www.teamwork.com/project-management-guide/project-management-methodologies/)
# MAGIC #### Waterfall vs Agile
# MAGIC
# MAGIC |                        | Waterfall                                                                                                                                                                                 | Agile                                                                                                                                                                       |
# MAGIC |------------------------|-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------|-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------|
# MAGIC | Project manager's role | Project manager serves as an active leader by prioritizing and assigning tasks to team members.                                                                                           | Agile project manager (or Scrum Master) acts primarily as a facilitator, removing any barriers the team faces.  Team shares more responsibility in managing their own work. |
# MAGIC | Scope                  | Project deliverables and plans are well-established and documented in the early stages of initiating and planning.  Changes go through a formal change request process.                   | Planning happens in shorter iterations and focuses on delivering value quickly.  Subsequent iterations are adjusted in response to feedback or unforeseen issues.           |
# MAGIC | Schedule               | Follows a mostly linear path through the initiating, planning, executing, and closing phases of the project.                                                                              | Time is organized into phases called Sprints. Each Sprint has a defined duration, with a set list of deliverables planned at the start of the Sprint.                       |
# MAGIC | Cost                   | Costs are kept under control by careful estimation up front and close monitoring throughout the life cycle of the project.                                                                | Costs and schedule could change with each iteration.                                                                                                                        |
# MAGIC | Quality                | Project manager makes plans and clearly defines criteria to measure quality at the beginning of the project.                                                                              | Team solicits ongoing stakeholder input and user feedback by testing products in the field and regularly implementing improvements.                                         |
# MAGIC | Communication          | Project manager continually communicates progress toward milestones and other key indicators to stakeholders, ensuring that the project is on track to meet the customer’s expectations.  | Team is customer-focused, with consistent communication between users and the project team.                                                                                 |
# MAGIC | Stakeholders           | Project manager continually manages and monitors stakeholder engagement to ensure the project is on track.                                                                                | Team frequently provides deliverables to stakeholders throughout the project. Progress toward milestones is dependent upon stakeholder feedback.                            |

